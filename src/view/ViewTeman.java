  private void tampilTeman() {
        koneksi.koneksiDB();

        DefaultTableModel tabel = new DefaultTableModel();

        tabel.addColumn("ID");
        tabel.addColumn("Nama Teman");
        tabel.addColumn("Nomor Handphone");
        tabel.addColumn("Email");

        TabelTeman.setModel(tabel);
        try {
            String sql = "select * from tbl_teman";

            ResultSet res = koneksi.s.executeQuery(sql);
            while (res.next()) {
                tabel.addRow(new Object[]{
                    res.getString("id"),
                    res.getString("nama"),
                    res.getString("nope"),
                    res.getString("email")
                });
                TabelTeman.setModel(tabel);

            }
        } catch (SQLException eer) {
            System.err.println("SQLException:" + eer.getMessage());
        }
    }

    private void tombolBaru() {
        txt_namaT.setEnabled(true);
        txt_nopeT.setEnabled(true);
        txt_emailT.setEnabled(true);
        txt_namaT.requestFocus();
        bersihTxtField();
    }

    private void tombolBatal() {
        txt_namaT.setEditable(false);
        txt_nopeT.setEditable(false);
        txt_emailT.setEditable(false);
        bersihTxtField();
        btn_simpanT.setEnabled(false);
        btn_baruT.setText("Baru");
    }
    
    private void bersihTxtField(){
        txt_idT.setText("");
        txt_namaT.setText("");
        txt_nopeT.setText("");
        txt_emailT.setText("");
    }
    
    private void tabelKlik(){
        btn_hapusT.setEnabled(true);
        btn_ubahT.setEnabled(true);
    }
    private void tabelUnKlik(){
        btn_hapusT.setEnabled(false);
        btn_ubahT.setEnabled(false);
        bersihTxtField();
    }

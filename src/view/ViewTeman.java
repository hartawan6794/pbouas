    private void btn_baruTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_baruTActionPerformed
        // TODO add your handling code here:
        String logic = btn_baruT.getText();
        if(logic.equals("Baru")){
            btn_simpanT.setEnabled(true);
            btn_baruT.setText("Batal");
            tombolBaru();
        }else{
            btn_simpanT.setEnabled(false);
            btn_baruT.setText("Baru");
            tombolBatal();
        }
    }//GEN-LAST:event_btn_baruTActionPerformed

    private void btn_simpanTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanTActionPerformed
        // TODO add your handling code here:
        String nama = txt_namaT.getText();
        String nope = txt_nopeT.getText();
        String email = txt_emailT.getText();
        if(nama.equals("")||nope.equals("")||email.equals("")){
            JOptionPane.showMessageDialog(null,"Harap isi Data dengan Benar");
        }else{
            if(JOptionPane.showConfirmDialog(null,"Yakin Ingin Menyimpan ? ","PERHATIAN",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                try{
                    mote.setNama(nama);
                    mote.setNope(nope);
                    mote.setEmail(email);
                    
                    conte.simpanDataTeman(mote);
                    tampilTeman();
                    tombolBatal();
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null,"Harap Isi Data Dengan Benar");
                }
            }
        }
    }                                           

    private void btn_ubahTActionPerformed(java.awt.event.ActionEvent evt) {                                          
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(null,"Apakah Anda Ingin Merubah ?","PERHATIAN",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            try{
                int id = Integer.parseInt(txt_idT.getText());
                String nama = txt_namaT.getText();
                String nope = txt_nopeT.getText();
                String email = txt_emailT.getText();
                
                mote.setId(id);
                mote.setNama(nama);
                mote.setNope(nope);
                mote.setEmail(email);
                
                conte.ubahDataTeman(mote);
                tampilTeman();
                tabelUnKlik();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,""+e.getMessage());
            }
        }else{
            
        }
    }//GEN-LAST:event_btn_simpanTActionPerformed

    private void btn_hapusTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusTActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(null,"Apakah Anda Ingin Menghapus ?","PERHATIAN",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            try{
                int id = Integer.parseInt(txt_idT.getText());
                mote.setId(id);
                conte.hapusTeman(mote);
                tampilTeman();
                tabelUnKlik();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, ""+e.getMessage());
            }
        }
    }//GEN-LAST:event_btn_hapusTActionPerformed

